#filesystem for OS
#currently, it doesn't support more than 2 users, for the whole OS

sudo mkdir /                            #root directory
sudo mkdir /home                        #where user stuff goes
sudo mkdir /var                         #important variables
sudo mkdir /boot                        #boot up files
sudo mkdir /home/apps                   #applications go here
sudo mkdir /home/apps/hackerstuff       #hacker stuff, e.g nmap
sudo mkdir /home/apps/productivity      #productivity apps
sudo mkdir /bin                         #bin for libraries
sudo mkdir /sbin                        #more libraries
sudo mkdir /sbin/bin                    #more libraries
sudo mkdir /home/userland               #user stuff
sudo mkdir /home/userland/media         #user media
sudo mkdir /home/userland/extern        #external storage devices
sudo mkdir /home/userland/extra         #other user stuff
sudo mkdir /root/apps                   #root user applications go here
sudo mkdir /root/apps/hackerstuff       #roothacker stuff, e.g nmap
sudo mkdir /root/apps/productivity      #root productivity apps
sudo mkdir /root/userland               #user stuff
sudo mkdir /root/userland/media         #root user media
sudo mkdir /root/userland/extern        #root external storage devices
sudo mkdir /root/userland/extra         #other root user stuff
